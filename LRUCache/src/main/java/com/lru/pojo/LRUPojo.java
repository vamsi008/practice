package com.lru.pojo;

public class LRUPojo {

	Integer id;

	String value;

	public LRUPojo(int id, String value) {
		this.id = id;
		this.value = value;

	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
